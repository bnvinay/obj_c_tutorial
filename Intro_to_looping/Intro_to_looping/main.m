#import <Foundation/Foundation.h>

int main(int argc, const char * argv[])
{

    @autoreleasepool {
        
        int x;
        // for loop to print x value from 1 to 10
        for(x=1;x<=10;x++)
            NSLog(@"%i",x);
    }
    return 0;
}

